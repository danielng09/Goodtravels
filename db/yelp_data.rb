def parse_yelp
  results = yelp['businesses']
  output = []
  results.each do |result|
    # next if result['review_count'] <= 50
    params = Hash.new
    params['title'] = result['name']
    params['location'] = result['location']['display_address'].join('\n')
    params['lat'] = result['location']['coordinate']['latitude']
    params['lng'] = result['location']['coordinate']['longitude']
    params['image_url'] = result['image_url'].gsub(/\/ms.jpg/,'/o.jpg')
    params['description'] = result['snippet_text']
    # params['review_count'] = result['review_count']
    # params['categories'] = result['categories']
    output.push(params)
  end
  output
end
